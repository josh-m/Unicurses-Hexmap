"""
blockstrings.py

Convert strings to 4x5 block characters.
"""


def convert(string):
    """Pass in a string, returns a list of 5 row strings"""
    string = string.upper()
    rows = ["", "", "", "", ""]

    for i in range(len(string)):
        b_char = BLOCK_CHAR[string[i]]
        for i in range(5):
            rows[i] += b_char[i]

    return rows

